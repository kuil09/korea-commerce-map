export function getServicesByCategory(categoryId: string): CommerceService[] {
  return commerceServices.filter((service) =>
    service.categories.includes(categoryId)
  );
}

export function getCategoryById(categoryId: string): Category | undefined {
  return categories.find((cat) => cat.id === categoryId);
}

export function searchServices(query: string): CommerceService[] {
  const lowerQuery = query.toLowerCase();
  return commerceServices.filter(
    (service) =>
      service.name.toLowerCase().includes(lowerQuery) ||
      service.nameEn.toLowerCase().includes(lowerQuery) ||
      service.description.toLowerCase().includes(lowerQuery) ||
      service.features.some((f) => f.toLowerCase().includes(lowerQuery))
  );
}
